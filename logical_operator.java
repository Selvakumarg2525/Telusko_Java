class logical_operator
{
    public static void main(String args[])
    {
        int x=5;
        int y=10;
        int a=34;
        int b=9;
        boolean result=a>b;

        System.out.println(result);
        System.out.println(a<b);
        System.out.println(x<=y);
        System.out.println(x>=y);
        System.out.println(x>y && a<=b);
        System.out.println(x>=y || a>=b);
        
    }
}